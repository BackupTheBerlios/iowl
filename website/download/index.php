<h1>Download</h1>
<p>
iOwl.net software is at an early stage of development. Nevertheless we would like to
share it with all the people around the world in order to give every beta tester the
chance  to influence the further development by his
<a href="../support/email.php">feedback</a>.
</p>
<p>
It would be very helpful to get as many
<a href="http://developer.berlios.de/bugs/?func=addbug&amp;group_id=250">
bug reports</a>
as possible so that we can soon present a stable release. Thanks in advance
for your feedback.
</p>
<p>
As this is an alpha version of iOwl.net there are some restrictions that will
be worked out in the future:
</p>
<ul>
  <li>The computer iOwl is running on must be connected to the Internet (having a valid
      IP-Address). Attention dial up users, iOwl.net tries to connect to the Internet
	at startup.</li>
  <li>Best recommendation results depend on the amount of owl that are actively running.
      The more owls are available on the net the better results are.</li>
  <li>Only html documents can be found, no .pdf or .doc</li>
</ul>

<p>
If you like to keep informed about updates please join the iOwl.net
<a href="http://lists.berlios.de/mailman/listinfo/iowl-users">mailing list</a>.
</p>


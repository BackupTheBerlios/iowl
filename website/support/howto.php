<p><a name="start"></a></p>
<p><b>Getting started</b></p>
<ol>
  <li>Download software (see <a href="/download/">downloading instructions</a>)</li>
  <li>Install iOwl.net software (see <a href="installation.php">installation guide</a>)</li>
  <li>Surf the web as usual</li>
  <li>Get the best of the web using recommendations (see <a href="#current">current
    recommendation</a> or <a href="#shortterm">shortterm recommendation</a>)</li>
</ol>

<p><a name="current"></a></p>
<p><b>How to find a related website (current recommendation)</b></p>
<p>Example: You are interested in soccer and you have found a soccer
  portal, e.g. soccer.com. This portal is very good, but you would
  like to find other websites which are related to soccer. Here
  support iOwl.net you. What do you have to do to find related websites
  without searching?

<ol>
  <li>Surf soccer.com (the website you would like to find related links to)</li>
  <li>Go to your Owl by typing "http://my.iOwl.net" into your webbrowser's
      location/address field</li>
  <li>You see iOwl's starting screen</li>
  <li>Click &quot;- get recommendations for this link&quot; near the soccer.com entry</li>
  <li>Wait a moment while iOwl.net finds the best related websites to "soccer.com" for you</li>
  <li>All the results are shown on the screen</li>
  <li>Select a related website among the list of results </li>
</ol>
<p>Assumption: iOwl.net software is up and running in the background
  and the browser uses iOwl.net software as proxy. The browser configuration
  is described <a href="browser.php">here</a>. </p>
<p><a name="shortterm"></a></p>
  <b>How to find related websites to a group of websites (shortterm
  recommendation) </b>
<p>Example: You are interested in sports in general (soccer, basketball,
   diving) and you would like to find websites that cover all the
   three kinds of sports. Follow the instructions:
<ol>
  <li>Visit a group of websites, e.g. soccer.com, basketball.com and diving.com</li>
  <li>Go to your iOwl.net by typing "http://my.iowl.net" into your webbrowser's location/address field</li>
  <li>You see iOwl's starting screen</li>
  <li>Select the button "recommendation"</li>
  <li>Wait a moment while iOwl.net finds related websites to the last few websites
      you surfed (soccer.com, basketball.com and diving.com)</li>
  <li>The results in this example should be sports-portals like sports.com</li>
</ol>

<p><a name="remove"></a></p>
<p><b>How to remove an element from the history</b></p>
<p>You can simply remove elements from the history by clicking<br>
   &quot;- remove from history.&quot; near the element.</p>

<p><a name="checkrun"></a></p>
<p><b>How to check if iOwl.net software is running (on Windows) </b></p>
<p>If you can see a iOwl.net Trayicon </p>
<p align="center"><img src="/pix/trayicon.gif" width="183" height="54"></p>
<p>near the clock then iOwl.net is running.</p>

<p><a name="manually"></a></p>
<p><b>How to start iOwl.net software manually (on Windows) </b></p>
<p>If the software doesn't start automatic.</p>
<p>Go to Start &gt; Programs &gt; iOwl.net and click &quot;Run iOwl.net&quot;.</p>
<p>Alternative go to the dos prompt:</p>
<ol>
  <li>change your directory (standard: CD  C:\Programs\iOwl)</li>
  <li>start iOwl.net by executing run_iowl.bat</li>
</ol>

<p><a name="service"></a></p>
<p><b>How to check if iOwl.net software is fit for service</b> </p>
<p>Type <a href="http://my.iowl.net/">http://my.iowl.net/</a> into
   your webbrowser. If the start page appears immediately, your browser
   is properly configured and the software is running. Otherwise
   either the <a href="browser.php">browser configuration</a> is
   corrupt or the <a href="#checkrun">software does not run</a>.</p>
